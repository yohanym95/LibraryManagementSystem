    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       String studentID = jTextField1.getText();
       String BookID = jTextField7.getText();
       String Query="Select * from issuebook";
       
                 PreparedStatement pst1;
        try {
            pst1 = (PreparedStatement) con.prepareStatement(Query);
            ResultSet rs = pst1.executeQuery();
             while(rs.next()){
                
                if(BookID.equals(rs.getString("BookId")) && studentID.equals(rs.getString("StudentId"))){
                   
                    String stName = rs.getString("StudentName");
                    jTextField2.setText(stName);
                    String Faculty = rs.getString("Faculty");
                    jTextField3.setText(Faculty);
                    String Course = rs.getString("Course");
                    jTextField4.setText(Course);
                    String Year = rs.getString("Year");
                    jTextField5.setText(Year);
                    String Sem = rs.getString("Semester");
                    jTextField6.setText(Sem);
                    String BookName = rs.getString("BookName");
                    jTextField8.setText(BookName);
                    String Publisher = rs.getString("Publisher");
                    jTextField9.setText(Publisher);
                    String Edition = rs.getString("Edition");
                    jTextField10.setText(Edition);
                    String Price = rs.getString("Price");
                    jTextField11.setText(Price);
                    String Pages = rs.getString("Pages");
                    jTextField12.setText(Pages);
                    String date = rs.getString("IssueDate");
                    jTextField13.setText(date);
                    
                }
             }
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, ""+e+"", "error", JOptionPane.ERROR_MESSAGE);
        }
       
    }//GEN-LAST:event_jButton3ActionPerformed

